// One-time-ish importer: content/game-bank/*.md → src/lib/generated/game-bank.ts
//
// Parses the subject content files (frontmatter + fenced ```yaml blocks under
// ## QUESTIONS / ## TEACHING) delivered by the PDF author, and emits a typed
// module the app merges over its hand-authored fallback. Re-run whenever a new
// subject file lands:  node scripts/import-game-bank.mjs
//
// Question blocks carry `type:` (mcq|short); teaching blocks carry `kind:`.

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const yaml = require("js-yaml");

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SRC_DIR = join(ROOT, "content", "game-bank");
const OUT = join(ROOT, "src", "lib", "generated", "game-bank.ts");

const VALID_KINDS = new Set(["definition", "precision", "qa", "careless", "sbq", "poa"]);

function fencedYamlBlocks(md) {
  const blocks = [];
  const re = /```ya?ml\s*\n([\s\S]*?)```/g;
  let m;
  while ((m = re.exec(md))) blocks.push(m[1]);
  return blocks;
}

function parseFrontmatter(md) {
  const m = md.match(/^---\s*\n([\s\S]*?)\n---/);
  if (!m) throw new Error("missing frontmatter");
  return yaml.load(m[1]);
}

const errors = [];
const sets = [];
const teaching = {};

const files = readdirSync(SRC_DIR)
  .filter((f) => f.endsWith(".md") && !f.startsWith("._"))
  .sort();

for (const file of files) {
  const md = readFileSync(join(SRC_DIR, file), "utf8");
  let fm;
  try {
    fm = parseFrontmatter(md);
  } catch (e) {
    errors.push(`${file}: ${e.message}`);
    continue;
  }
  const { level, slug, subjectName, family } = fm;
  if (!level || !slug || !family) {
    errors.push(`${file}: frontmatter needs level, slug, family`);
    continue;
  }

  const questions = [];
  const cards = [];
  let qn = 0;

  for (const raw of fencedYamlBlocks(md)) {
    let doc;
    try {
      doc = yaml.load(raw);
    } catch (e) {
      errors.push(`${file}: bad yaml block — ${e.message}`);
      continue;
    }
    if (!doc || typeof doc !== "object") continue;

    // ── a question ──────────────────────────────────────────────────────
    if (doc.type === "mcq" || doc.type === "short") {
      qn += 1;
      const id = `${slug}-g${qn}`;
      if (!doc.topic || !doc.stem) {
        errors.push(`${file} ${id}: question needs topic + stem`);
        continue;
      }
      let correctKey;
      let options;
      if (doc.type === "mcq") {
        options = (doc.options ?? []).map((o) => String(o));
        if (options.length < 2) {
          errors.push(`${file} ${id}: mcq needs >=2 options`);
          continue;
        }
        if (typeof doc.answer !== "number" || doc.answer < 0 || doc.answer >= options.length) {
          errors.push(`${file} ${id}: mcq answer index out of range`);
          continue;
        }
        correctKey = [String(doc.answer)];
      } else {
        const accepted = (doc.accepted ?? []).map((a) => String(a));
        if (accepted.length === 0) {
          errors.push(`${file} ${id}: short needs accepted[]`);
          continue;
        }
        correctKey = accepted;
      }
      questions.push({
        id,
        type: doc.type,
        topic: String(doc.topic),
        stem: String(doc.stem),
        ...(options ? { options } : {}),
        correctKey,
        marks: Number(doc.marks ?? 1),
        workedSolution: String(doc.worked ?? "").trim(),
        misconceptionTag: String(doc.misconception ?? "unset"),
        mapsToProduct: "vault",
      });
      continue;
    }

    // ── a teaching card ─────────────────────────────────────────────────
    if (typeof doc.kind === "string") {
      if (!VALID_KINDS.has(doc.kind)) {
        errors.push(`${file}: unknown teaching kind "${doc.kind}"`);
        continue;
      }
      cards.push(doc);
      continue;
    }
  }

  if (questions.length === 0) errors.push(`${file}: no questions parsed`);

  sets.push({
    level,
    slug,
    topicLabel: subjectName ? `${subjectName} — predicted topics` : "Predicted topics",
    questions,
  });
  teaching[`${level}/${slug}`] = cards;

  const mcq = questions.filter((q) => q.type === "mcq").length;
  console.log(
    `✓ ${file}: ${questions.length} questions (${mcq} mcq) · ${cards.length} teaching cards`
  );
}

if (errors.length) {
  console.error("\n✗ import errors:\n" + errors.map((e) => "  - " + e).join("\n"));
  process.exit(1);
}

mkdirSync(dirname(OUT), { recursive: true });
const banner =
  "// AUTO-GENERATED by scripts/import-game-bank.mjs — DO NOT EDIT BY HAND.\n" +
  "// Source: content/game-bank/*.md. Re-run the script to regenerate.\n";
const body =
  banner +
  'import type { DiagnosticSet } from "@/lib/diagnostic-questions";\n\n' +
  "export interface ImportedCard {\n  kind: string;\n  [key: string]: unknown;\n}\n\n" +
  "export const IMPORTED_SETS: DiagnosticSet[] = " +
  JSON.stringify(sets, null, 2) +
  ";\n\n" +
  "export const IMPORTED_TEACHING: Record<string, ImportedCard[]> = " +
  JSON.stringify(teaching, null, 2) +
  ";\n";

writeFileSync(OUT, body);
console.log(`\n✓ wrote ${OUT} — ${sets.length} subject set(s).`);
